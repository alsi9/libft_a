#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*dest;
	size_t	cs1;
	size_t	cs2;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (0);
	cs1 = ft_strlen(s1);
	cs2 = ft_strlen(s2);
	dest = (char *)malloc(sizeof(char) * (cs1 + cs2 + 1));
	if (!dest)
		return (0);
	while (s1[i])
		dest[j++] = s1[i++];
	i = 0;
	while (s2[i])
		dest[j++] = s2[i++];
	dest[j] = '\0';
	return (dest);
}
